    public function FileManager($name, $default = null, $label = "Select File", $multiple = false, $type = "images")
    {
        $view = 'single';
        if ($multiple) {
            $view = 'multiple';
        }
        $target = self::slugify($name);
        $options = [
            'label' => $label,
            'name' => $name,
            'selected_image' => $default,
            'target' => $target,
            'helper' => new MainHelper()
        ];
        return view('backend.partials.filemanager.types.' . $view, $options);
    }

