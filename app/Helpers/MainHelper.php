    function snakeToCamel($string)
    {
        return lcfirst(str_replace(' ', '', ucwords(str_replace('_', ' ', $string))));
    }

    function camelToSnake($string)
    {
        return strtolower(preg_replace('/([a-z])([A-Z])/', '$1_$2', $string));
    }

    function camelCaseToWords($camelCaseString)
    {
        $words = preg_replace('/(?<!^)([A-Z])/', ' $1', $camelCaseString);
        return ucwords($words);
    }

    public function calcPrice($vehicle, $start_date, $end_date, $extras = [])
    {
        $datetime1 = new DateTime($start_date);
        $datetime2 = new DateTime($end_date);
        $interval = $datetime1->diff($datetime2);
        $days = $interval->format('%a');
        $total = $vehicle->daily_price * $days;
        if ($vehicle->prices) {
            foreach ($vehicle->prices as $price) {
                if ($days >= $price['from'] && $days <= $price['to']) {
                    $total = $price['price'] * $days;
                }
            }
        }
        $extra_total = 0;
        if ($extras) {
            $extras = ExtraService::whereIn('uniq_key', $extras)->where('lang', config('app.locale'))->get();
            foreach ($extras as $extra) {
                $extra_total = $extra_total + $this->calcExtras($extra, $days);
            }
        }
        return $total + $extra_total;
    }

    public function calcExtras($extra, $day)
    {
        if ($extra->type == 0) {
            return $extra->price * $day;
        } else {
            return $extra->price;
        }
    }

    public function price($price, $symbol = '', $float = 'left'): string
    {
        $price = number_format($price, 2);
        $price = $symbol . $price;
        if ($float == 'right') {
            $price = $price . $symbol;
        }
        return $price;
    }

    public function default_lang($languages)
    {
        foreach($languages as $lang){
